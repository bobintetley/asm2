<H2><A NAME="SECTION000221000000000000000">
Can I use MySQL for my database?</A>
</H2>

<P>
A. Yes. The procedure for Windows users is:

<OL>
<LI>Download and install MySQL from www.mysql.com. It will set up Windows
services for the databases, etc. etc.
<BR>
<P>
</LI>
<LI>Copy the mysql.sql file from C:&#92;Program Files&#92;ASM&#92;data&#92;sql
to C:&#92;
<BR>
<P>
</LI>
<LI>Open a MySQL console. If there's no start menu item for it, go to
C:&#92;Program Files&#92;MySQL&#92;bin
and double click mysql
<BR>
<P>
</LI>
<LI>Enter the following SQL commands at the mysql&gt; prompt: 
<BR>
<BR><SPAN  CLASS="textit">CREATE DATABASE asm; </SPAN>
<BR><SPAN  CLASS="textit">USE asm; </SPAN>
<BR><SPAN  CLASS="textit">SOURCE c:&#92;mysql.sql </SPAN>
<BR><SPAN  CLASS="textit">(lots of output will be displayed) </SPAN>
<BR><SPAN  CLASS="textit">GRANT ALL PRIVILEGES ON *.* TO root@'%'; </SPAN>
<BR><SPAN  CLASS="textit">QUIT;</SPAN>
<BR>
<P>
</LI>
<LI>Edit the file: 
<BR>
<BR><SPAN  CLASS="textit">C:&#92;Documents and Settings/YOURUSER/.asm/jdbc.properties</SPAN>
<BR>
<P>
</LI>
<LI>Replace any existing contents of the file with this line:
<BR>
<BR><SPAN  CLASS="textit">JDBCURL=jdbc:mysql://localhost/asm?user=root&amp;zeroDateTimeBehaviour=convertToNull</SPAN>
<BR>
<P>
</LI>
<LI>For each machine on your network, substitute <SPAN  CLASS="textit">localhost</SPAN>
in the above line of that file for the IP address of the machine running
MySQL.
</LI>
</OL>
For Linux/other platform users the procedure is the same, except you
should install MySQL from your distribution and ``cd'' to the
asm/data/sql directory and just use ``SOURCE mysql.sql'' when
executing the database commands. Documents and Settings can be replaced
with $HOME.

