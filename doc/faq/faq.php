<H2><A NAME="SECTION000213000000000000000">
Do I have to use the inbuilt report viewer?</A>
</H2>

<P>
A. As reports are generated as HTML, you are free to use an external
browser for viewing and printing them instead of Animal Shelter Manager's
built in report viewer. To do this, go to the Preferences-&gt;Settings
menu option and untick the Use internal browser
box. ASM will open reports with whatever association it has for html
files (see System-&gt;Configure File Types)

